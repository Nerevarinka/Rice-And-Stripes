type AnchorToElementProps = {
    caption: string;

    elementId: string;

    className?: string;
};

const AnchorToElement: FC<AnchorToElementProps> = ({
    caption, elementId,
    className
}) => {
    const onClick = useCallback(() => {
        const element = document.getElementById(elementId);

        if (!element) {
            return;
        }

        element.scrollIntoView({
            behavior: "smooth",

        });
    }, [elementId]);

    return (
        <a
            onClick={onClick}
            className={className}
            style={{ cursor: "pointer" }}
        >
            {caption}
        </a>
    );
};
